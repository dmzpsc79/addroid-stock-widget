    public static List<StockItem> search(String query, int limit) {
        String q = query == null ? "" : query.trim().toUpperCase();
        List<StockItem> results = new ArrayList<>();
        if (q.isEmpty()) {
            return results;
        }
        for (StockItem item : ITEMS) {
            if (item.code.contains(q) || item.name.toUpperCase().contains(q)) {
                results.add(item);
                if (results.size() >= limit) {
                    break;
                }
            }
        }
        return results;
    }
